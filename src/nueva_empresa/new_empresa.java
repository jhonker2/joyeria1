    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
if(txt_nombre_empresa.getText().isEmpty()||txt_gerente.getText().isEmpty()||txt_direccion.getText().isEmpty()||txt_telefono.getText().isEmpty())
{
joyeria1.Joyeria1.ven.Mensaje_Error("Por favor complete los Campos Vacios", "Campos Vacios");
}else
{
        Object [] datos = new Object[4];
        datos[0]=txt_nombre_empresa.getText();
        datos[1]=txt_gerente.getText();
        datos[2]=txt_direccion.getText();
        datos[3]=txt_telefono.getText();
        Object [] campos = new Object[4];
        campos[0]="nombre_empresa";
        campos[1]="gerente";
        campos[2]="direccion";
        campos[3]="telefono";
        insertar_registro.InsertaRegistro("empresas",campos,datos);
        joyeria1.Joyeria1.ven.Mensaje("Empresa Registrada en el sistema","Registro Exitoso" );
        this.dispose();
        obj.setVisible(true);
        obj.setLocationRelativeTo(null);
}
    }//GEN-LAST:event_btn_guardarActionPerformed

