    private void btn_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registrarActionPerformed
      String cede="0",client="0";
      String maestro= jComboBox1.getSelectedItem().toString();
        Date fecha= fecha_entrega.getDate();
            SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");

        cede=txt_cedula.getText();
        client=txt_cliente.getText();
        Object [] datos  = new Object[10];
        datos[0]=lb_fecha_actual.getText();
        datos[1]=formato.format(fecha);
        datos[2]=txt_hora.getText();
        datos[3]=cede;
        datos[4]=txt_pedido.getText();
        datos[5]=txt_precio.getText();
        datos[6]=txt_abono.getText();
        datos[7]=txt_total.getText();
        datos[8]="No entregado";
        datos[9]=obj1.buscar_cedula(maestro);
        Object [] campos = new Object[10];
        campos[0]="fecha_pedido";
        campos[1]="fecha_entrega";
        campos[2]="hora_entrega";
        campos[3]="cedula";
        campos[4]="pedido";
        campos[5]="precio";
        campos[6]="abono";
        campos[7]="total";
        campos[8]="estado";
        campos[9]="cedula_maestro";
        insertar_registro.InsertaRegistro("pedidos",campos,datos);
        joyeria1.Joyeria1.ven.Mensaje("Pedido Registrado...", "Pedidos");
        limpiar();
    }//GEN-LAST:event_btn_registrarActionPerformed

